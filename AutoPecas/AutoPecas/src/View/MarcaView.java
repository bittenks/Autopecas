    private void btnSalvarMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarMarcaActionPerformed
        if ( txtMarcaView.getText().isEmpty()
             ){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
            txtMarcaView.requestFocusInWindow();
        } else{
            marca = new Marca();
            marca.setDescricao(txtMarcaView.getText());
                     

            try {
                marcaDAO.salvar(marca);
            } catch (SQLException ex) {
                Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Categoria Salva com Sucesso!!!");
           atualizarTabelaProduto();
           preparaSalvareCancelar();
           desativaCampos();
           limpaCamposProduto();                 
        }
    }//GEN-LAST:event_btnSalvarMarcaActionPerformed

    private void btnExcluirMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirMarcaActionPerformed
        if (txtIdMarcaView.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Selecione a Categoria");
        }else{
            marca = new Marca();
            marca.setIdmarcaProd(Integer.parseInt(txtIdMarcaView.getText()));
            int confirma = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + txtIdMarcaView.getText());
            if (confirma == 0){
                try {
                    marcaDAO.excluir(marca);
                } catch (SQLException ex) {
                    Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "Categoria Excluida com Sucesso!!!");
                limpaCamposProduto();
                atualizarTabelaProduto();
                preparaExcluir();    
            }
        }
    }//GEN-LAST:event_btnExcluirMarcaActionPerformed

