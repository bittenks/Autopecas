    public void atualizarTabelaProduto(){
        marca = new Marca();
            try {
                listaMarca = marcaDAO.ListaMarca();
            } catch (SQLException ex) {
                Logger.getLogger(ClienteView.class.getName()).log(Level.SEVERE, null, ex);
            }
                String dados [][] = new String[listaMarca.size()][2];
                int i = 0;
               for (Marca produto : listaMarca){
                    dados[i][0] = String.valueOf(produto.getIdmarcaProd());
                    dados[i][1] = produto.getDescricao();                    
                    i++;
                }
                String tituloColuna[] = {"IdMarca", "Descrição"};
                DefaultTableModel tabelaMarca = new DefaultTableModel();
                tabelaMarca.setDataVector(dados, tituloColuna);
                tblMarca.setModel(new DefaultTableModel(dados, tituloColuna){
                    boolean[] canEdit = new boolean[]{
                        false, false/*, false*/
                    };
                   
                        public boolean isCellEdiTable(int rowIndex, int columnIndex){
                            return canEdit[columnIndex];
                        }
                });
                tblMarca.getColumnModel().getColumn(0).setPreferredWidth(100);
                tblMarca.getColumnModel().getColumn(1).setPreferredWidth(100);           

                
                
                DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
                centralizado.setHorizontalAlignment(SwingConstants.CENTER);
                tblMarca.getColumnModel().getColumn(0).setCellRenderer(centralizado);
                tblMarca.setRowHeight(25);
                tblMarca.updateUI();
    }
    
